DSEG SEGMENT
    DATA DB 12,13,14
    POST DB'+$'
    NEGT DB'-$'
    NONE DB ' '
DSEG ENDS

SSEG SEGMENT STACK
    STK DB 50 DUP(0)
SSEG ENDS

CSEG SEGMENT
    ASSUME CS:CSEG,DS:DSEG,SS:SSEG

START:  MOV AX,DSEG
        MOV DS,AX
        MOV AX,SSEG
        MOV SS,AX
        MOV SP,SIZE STK

        MOV AL,DATA
        CMP AL,0
        JG  POSITIVE
        JGE SPACE
        JNGE NEGTIVE

POSITIVE:MOV AL,DATA+1
        CMP AL,0
        JG POSITIVE2
        JNG SPACE

POSITIVE2:MOV AL,DATA+2
        CMP AL,0
        JG POSOUTPUT
        JNG SPACE

POSOUTPUT:MOV DX,OFFSET POST
        MOV AH,09H
        INT 21H
        JMP EXIT


NEGTIVE:MOV AL,DATA+1
        CMP AL,0
        JGE SPACE
        JNGE NEGTIVE2

NEGTIVE2:MOV AL,DATA+2
         CMP AL,0
         JGE SPACE
         JNGE NGEOUTPUT

NGEOUTPUT:MOV DX,OFFSET NEGT
        MOV AH,09H
        INT 21H
        JMP EXIT

SPACE:  MOV BX,OFFSET NONE
        MOV DL,[BX]
        MOV AH,02H
        INT 21H
        JMP EXIT

EXIT: MOV AH,4CH
      INT 21H

CSEG ENDS
END START