DSEG SEGMENT
    NUM DB 01H,02H,03H,81H,82H,83H
    COUNT DW 6
    MEANP DW 0
    MEANM DW 0
DSEG ENDS

CSEG SEGMENT
    ASSUME CS:CSEG,DS:DSEG
START:    MOV AX,DSEG
    MOV DS,AX
    MOV SI,OFFSET NUM
    MOV CX,COUNT

    XOR BX,BX
    XOR DX,DX

LP: MOV BL,[SI]
    TEST BL,BL
    JZ NEXT
    JS NEGT
    JMP POSTIVE

NEXT:INC SI
    CMP CX,0
    JZ TT
    LOOP LP

POSTIVE:
    MOV BH,00H
    ADD MEANP,BX
    INC SI
    INC DH ;正数的个数
    CMP CX,0
    JZ TT
    LOOP LP

NEGT:
    MOV BH,0FFH
    ADD MEANM,BX
    INC SI
    INC DL
    CMP CX,0
    JZ TT
    LOOP LP

TT:   TEST DH,DH
    JZ JUDGENEG
    MOV AX,MEANP
    DIV DH
    MOV AH,00H
    MOV MEANP,AX

JUDGENEG:TEST DL,DL
    JZ EXIT
    MOV AX,MEANM
    IDIV DL
    MOV AH,0FFH
    MOV MEANM,AX

EXIT:MOV AH,4CH
    INT 21H

CSEG ENDS
END START


